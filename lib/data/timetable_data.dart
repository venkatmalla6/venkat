// ─── Data Models & Content for all 3 Timetables ───────────────────────────

class ScheduleRow {
  final String time;
  final String topic;
  final String activity;
  final bool isBreak;
  const ScheduleRow(this.time, this.topic, this.activity,
      {this.isBreak = false});
}

class QAItem {
  final String question;
  final String answer;
  const QAItem(this.question, this.answer);
}

class DayData {
  final int dayNumber;
  final String title;
  final String subtitle;
  final List<String> tags;
  final List<ScheduleRow> schedule;
  final List<String> covered;
  final List<QAItem> qa;
  const DayData({
    required this.dayNumber,
    required this.title,
    required this.subtitle,
    required this.tags,
    required this.schedule,
    required this.covered,
    required this.qa,
  });
}

class TimetableData {
  final String id;
  final String label;
  final String emoji;
  final String subtitle;
  final List<DayData> days;
  const TimetableData({
    required this.id,
    required this.label,
    required this.emoji,
    required this.subtitle,
    required this.days,
  });
}

